/**
 * Sync docs/legal/zh/*.md into apps/client/src/data/legal-documents.ts for weapp offline display.
 * Run: node scripts/bundle-legal-docs.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const legalRoot = path.join(root, "docs/legal/zh");
const manifest = JSON.parse(fs.readFileSync(path.join(legalRoot, "manifest.json"), "utf8"));
const outPath = path.join(root, "apps/client/src/data/legal-documents.ts");

const docs = manifest.documents.map((doc) => {
  const content = fs.readFileSync(path.join(legalRoot, doc.file), "utf8");
  return {
    slug: doc.slug,
    title: doc.title,
    version: doc.version,
    content,
  };
});

const body = `/** Auto-generated by scripts/bundle-legal-docs.mjs — do not edit manually. */
export type BundledLegalDocument = {
  slug: string;
  title: string;
  version: string;
  content: string;
};

const DOCUMENTS: Record<string, BundledLegalDocument> = ${JSON.stringify(
  Object.fromEntries(docs.map((d) => [d.slug, d])),
  null,
  2,
)};

export function getBundledLegalDocument(slug: string): BundledLegalDocument | undefined {
  return DOCUMENTS[slug];
}

export function listBundledLegalDocuments(): BundledLegalDocument[] {
  return Object.values(DOCUMENTS);
}
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, body, "utf8");
console.log(`Wrote ${docs.length} legal documents to ${outPath}`);
